interface ConfigurableSimpleEditorProps {
  content: string;
  onUpdate: (html: string) => void;
  placeholder?: string;
  showActions?: boolean;
  onCancel?: () => void;
  onSave?: () => void;
  onExport?: () => void;
  isSaving?: boolean;
  isExporting?: boolean;
}

const MainToolbarContent = ({
  onHighlighterClick,
  onLinkClick,
  isMobile,
  showActions,
  onCancel,
  onSave,
  onExport,
  isSaving,
  isExporting,
}: {
  onHighlighterClick: () => void;
  onLinkClick: () => void;
  isMobile: boolean;
  showActions?: boolean;
  onCancel?: () => void;
  onSave?: () => void;
  onExport?: () => void;
  isSaving?: boolean;
  isExporting?: boolean;
}) => {
  return (
    <>
      {/* <Spacer /> */}

      <ToolbarGroup>
        <UndoRedoButton action="undo" />
        <UndoRedoButton action="redo" />
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <HeadingDropdownMenu levels={[1, 2, 3, 4]} portal={isMobile} />
        <ListDropdownMenu
          types={["bulletList", "orderedList", "taskList"]}
          portal={isMobile}
        />
        <BlockquoteButton />
        <CodeBlockButton />
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <MarkButton type="bold" />
        <MarkButton type="italic" />
        <MarkButton type="strike" />
        <MarkButton type="code" />
        <MarkButton type="underline" />
        {!isMobile ? (
          <ColorHighlightPopover />
        ) : (
          <ColorHighlightPopoverButton onClick={onHighlighterClick} />
        )}
        {!isMobile ? <LinkPopover /> : <LinkButton onClick={onLinkClick} />}
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <MarkButton type="superscript" />
        <MarkButton type="subscript" />
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <TextAlignButton align="left" />
        <TextAlignButton align="center" />
        <TextAlignButton align="right" />
        <TextAlignButton align="justify" />
      </ToolbarGroup>

      <ToolbarSeparator />

      <ToolbarGroup>
        <ImageUploadButton text="Add" />
      </ToolbarGroup>

      <Spacer />

      {showActions && !isMobile && (
        <>
          {/* <ToolbarSeparator /> */}
          <ToolbarGroup>
            {onCancel && (
              <Button
                data-style="ghost"
                onClick={onCancel}
                disabled={isSaving || isExporting}
                tooltip="Cancel"
                aria-label="Cancel"
              >
                <X className="tiptap-button-icon" />
              </Button>
            )}
            {onSave && (
              <Button
                data-style="ghost"
                onClick={onSave}
                disabled={isSaving || isExporting}
                tooltip={isSaving ? "Saving..." : "Save as New Version"}
                aria-label="Save as New Version"
              >
                {isSaving ? (
                  <Loader2 className="tiptap-button-icon animate-spin" />
                ) : (
                  <Save className="tiptap-button-icon" />
                )}
              </Button>
            )}
            {onExport && (
              <Button
                data-style="ghost"
                onClick={onExport}
                disabled={isSaving || isExporting}
                tooltip={isExporting ? "Exporting..." : "Add to Documents"}
                aria-label="Add to Documents"
              >
                {isExporting ? (
                  <Loader2 className="tiptap-button-icon animate-spin" />
                ) : (
                  <FileDown className="tiptap-button-icon" />
                )}
              </Button>
            )}
          </ToolbarGroup>
        </>
      )}
    </>
  );
};

const MobileToolbarContent = ({
  type,
  onBack,
}: {
  type: "highlighter" | "link";
  onBack: () => void;
}) => (
  <>
    <ToolbarGroup>
      <Button data-style="ghost" onClick={onBack}>
        <ArrowLeftIcon className="tiptap-button-icon" />
        {type === "highlighter" ? (
          <HighlighterIcon className="tiptap-button-icon" />
        ) : (
          <LinkIcon className="tiptap-button-icon" />
        )}
      </Button>
    </ToolbarGroup>

    <ToolbarSeparator />

    {type === "highlighter" ? (
      <ColorHighlightPopoverContent />
    ) : (
      <LinkContent />
    )}
  </>
);

export function ConfigurableSimpleEditor({
  content,
  onUpdate,
  placeholder = "Start typing...",
  showActions = false,
  onCancel,
  onSave,
  onExport,
  isSaving = false,
  isExporting = false,
}: ConfigurableSimpleEditorProps) {
  const isMobile = useIsMobile();
  const { height } = useWindowSize();
  const [mobileView, setMobileView] = useState<"main" | "highlighter" | "link">(
    "main"
  );
  const toolbarRef = useRef<HTMLDivElement>(null);

  const editor = useEditor({
    immediatelyRender: false,
    shouldRerenderOnTransaction: false,
    editorProps: {
      attributes: {
        autocomplete: "off",
        autocorrect: "off",
        autocapitalize: "off",
        "aria-label": placeholder,
        class: "simple-editor",
      },
    },
    extensions: [
      StarterKit.configure({
        horizontalRule: false,
        link: {
          openOnClick: false,
          enableClickSelection: true,
        },
      }),
      HorizontalRule,
      TextAlign.configure({ types: ["heading", "paragraph"] }),
      TaskList,
      TaskItem.configure({ nested: true }),
      Highlight.configure({ multicolor: true }),
      Image,
      Typography,
      Superscript,
      Subscript,
      Selection,
      Table.configure({
        resizable: true,
      }),
      TableRow,
      TableHeader,
      TableCell,
      ImageUploadNode.configure({
        accept: "image/*",
        maxSize: MAX_FILE_SIZE,
        limit: 3,
        upload: handleImageUpload,
        onError: (error) => console.error("Upload failed:", error),
      }),
    ],
    content,
    onUpdate: ({ editor }) => {
      onUpdate(editor.getHTML());
    },
  });

  // Update content when prop changes
  useEffect(() => {
    if (editor && content !== editor.getHTML()) {
      editor.commands.setContent(content);
    }
  }, [content, editor]);

  const rect = useCursorVisibility({
    editor,
    overlayHeight: toolbarRef.current?.getBoundingClientRect().height ?? 0,
  });

  useEffect(() => {
    if (!isMobile && mobileView !== "main") {
      setMobileView("main");
    }
  }, [isMobile, mobileView]);

  if (!editor) {
    return null;
  }

  return (
    <div
      className="configurable-simple-editor-wrapper"
      style={{
        width: "100%",
        height: "100%",
        minHeight: "500px",
        display: "flex",
        flexDirection: "column",
      }}
    >
      <EditorContext.Provider value={{ editor }}>
        <Toolbar
          ref={toolbarRef}
          className="w-full"
          style={{
            ...(isMobile
              ? {
                  bottom: `calc(100% - ${height - rect.y}px)`,
                }
              : {}),
          }}
        >
          {mobileView === "main" ? (
            <MainToolbarContent
              onHighlighterClick={() => setMobileView("highlighter")}
              onLinkClick={() => setMobileView("link")}
              isMobile={isMobile}
              showActions={showActions}
              onCancel={onCancel}
              onSave={onSave}
              onExport={onExport}
              isSaving={isSaving}
              isExporting={isExporting}
            />
          ) : (
            <MobileToolbarContent
              type={mobileView === "highlighter" ? "highlighter" : "link"}
              onBack={() => setMobileView("main")}
            />
          )}
        </Toolbar>

        <EditorContent
          editor={editor}
          role="presentation"
          className="simple-editor-content"
          style={{ flex: 1, overflow: "auto" }}
        />
      </EditorContext.Provider>
    </div>
  );
}
